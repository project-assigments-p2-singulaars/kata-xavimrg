function sumar(a, b) {
    return a + b;
}

function restar(c, d) {
    return c - d
}

function multiplicar(e , f) {
    return e * f;
}

function dividir(g , h) {
    return g / h;
}

module.exports = {
    sumar
}

module.exports = {
    restar
}

module.exports = {
    dividir
}

module.exports = {
    multiplicar
}